function navActive(path: string, href: string): boolean {
  if (path === href) return true;
  if (href !== "/" && path.startsWith(`${href}/`)) return true;
  return false;
}

function item(label: string, href: string, path: string): NavItem {
  return { label, href, active: navActive(path, href) };
}

function group(name: string, items: Array<[string, string]>, path: string): NavGroup {
  const navItems = items.map(([label, href]) => item(label, href, path));
  return {
    is_group: true,
    group: name,
    open: navItems.some((i) => i.active),
    items: navItems,
  };
}

